using EdcHost.SlaveServers.EventArgs;
using Xunit;

namespace EdcHost.Tests.UnitTests.SlaveServers.EventArgs;

public class PlayerTryUseEventArgsTests
{
    public void PlayerTryUseEventArgs_CorrectlyIntialized()
    {
        int expectedID = 2022;
        int expectedChunk = 32;
        PlayerTryUseEventArgs args = new PlayerTryUseEventArgs(2022, 32);
        Assert.Equal(expectedID, args.PlayerId);
        Assert.Equal(expectedChunk, args.TargetChunk);
        Assert.Equal("PLAYER_TRY_USE", args.EventType);
    }
}
